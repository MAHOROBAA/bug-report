export function useRandomMessage() {
  const messages = [
    '오늘도 고생했어요. 이건 용기 있는 기록이에요.',
    '기록은 변화를 만드는 첫걸음이에요.',
    '잘했어요. 이렇게 솔직히 남겨줘서 고마워요.',
    '이건 분명 누군가에게 도움이 될 거예요.',
    '좋아요, 한 걸음 나아갔어요.',
    '이 기록 덕분에 팀이 조금 더 좋아질 거예요.',
    '솔직한 말, 그게 진짜 용기예요.',
    '이건 단순한 불만이 아니라 발전의 씨앗이에요.',
    '기록을 남겼다는 건, 마음을 정리했다는 뜻이에요.',
    '감정이 아닌 경험을 남기는 건 대단한 일이에요.',
    '고마워요. 진심으로.',
    '이 리포트는 단순한 글이 아니에요. 변화의 시작이에요.',
    '용기 냈네요. 멋져요.',
    '다음엔 조금 더 가벼운 하루가 오길 바라요.',
    '기록이 쌓일수록 우리가 단단해져요.',
    '이건 단순한 보고가 아니라 신호예요.',
    '진짜 잘했어요. 덕분에 더 나은 환경이 돼요.',
    '솔직한 기록이 결국 시스템을 바꿔요.',
    '오늘의 기록은 내일의 개선이에요.',
    '진심으로 수고했어요. 이제 조금 쉬어요.'
  ];

  const getRandomMessage = () => {
    const index = Math.floor(Math.random() * messages.length);
    return messages[index];
  };

  return { getRandomMessage };
}
